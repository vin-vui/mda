            <p>
                La Maison de l'Adolescent est un lieu de soin public, confidentiel et gratuit.
                Ouverte aux adolescents, leurs parents et entourage mais aussi aux professionnels intervenants auprès d'adolescents, la Maison de l'Adolescent offre un lieu d'accueil, d'écoute, d'information, d'accompagnement et d'orientations gratuites et confidentielles à tous les adolescents seuls ou accompagnés d'un proche ou d'un professionnel.
            </p>
            <p>
                Elle apporte des réponses de santé globale et plus largement prend soin de l'adolescent dans ses dimensions psychiques, sociales, éducatives, scolaires, juridiques, somatiques. Elle assure un soutien à leurs parents et propose un appui aux professionnels (enseignants, éducateurs, travailleurs sociaux, médecins généralistes, institutions…) pour des conseils et avis ou orientation.
            </p>
